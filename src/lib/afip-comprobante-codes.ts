/**
 * Normaliza un código de comprobante leído por la IA a la clave de 3 dígitos
 * de la tabla AFIP (ej. "1", "01", "001", " 6 " → "001"/"006").
 * Devuelve null si no es un número de 1 a 3 dígitos.
 */
export function normalizeAfipComprobanteCode(
  raw: string | null | undefined,
): string | null {
  if (raw == null) return null;
  const digits = String(raw).replace(/\D/g, "");
  if (digits.length === 0 || digits.length > 3) return null;
  const n = Number(digits);
  if (!Number.isInteger(n) || n <= 0) return null;
  return String(n).padStart(3, "0");
}

/**
 * true si el código leído corresponde a un comprobante fiscal de la tabla AFIP.
 */
export function isKnownAfipComprobanteCode(
  raw: string | null | undefined,
): boolean {
  const code = normalizeAfipComprobanteCode(raw);
  if (!code) return false;
  return code in AFIP_COMPROBANTE_CODES;
}

/** Descripción oficial AFIP del código, o null si no se reconoce. */
export function describeAfipComprobanteCode(
  raw: string | null | undefined,
): string | null {
  const code = normalizeAfipComprobanteCode(raw);
  if (!code) return null;
  return AFIP_COMPROBANTE_CODES[code] ?? null;
}
